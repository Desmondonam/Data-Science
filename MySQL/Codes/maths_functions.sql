-- Ceil
SELECT CEIL(10.5);

-- Random
SELECT RAND();

-- set seed
SELECT RAND(1234);

-- generate random numbers using the same seed
SELECT RAND(), RAND(), RAND();

-- round off
SELECT ROUND(2.7656, 2);

-- Power
SELECT POWER(2, 3);

-- Absolute
SELECT ABS(-4);

-- floor
SELECT FLOOR(10.5);

-- square root function
SELECT SQRT(16);

-- exponential
SELECT EXP(1);

-- log function
SELECT LOG(10);
SELECT LOG10(100);